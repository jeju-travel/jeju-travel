package formerror.lodging;

public class LodgingError {
	private String lodging_nameErr;
	private String lodging_locErr;
	private String lodging_phoneErr;
	private String lodging_priceErr;
	private String lodging_imageErr;
	private boolean result;
	
	public LodgingError() {}

	public String getLodging_nameErr() {
		return lodging_nameErr;
	}

	public void setLodging_nameErr(String lodging_nameErr) {
		this.lodging_nameErr = lodging_nameErr;
	}

	public String getLodging_locErr() {
		return lodging_locErr;
	}

	public void setLodging_locErr(String lodging_locErr) {
		this.lodging_locErr = lodging_locErr;
	}

	public String getLodging_phoneErr() {
		return lodging_phoneErr;
	}

	public void setLodging_phoneErr(String lodging_phoneErr) {
		this.lodging_phoneErr = lodging_phoneErr;
	}

	public String getLodging_priceErr() {
		return lodging_priceErr;
	}

	public void setLodging_priceErr(String lodging_priceErr) {
		this.lodging_priceErr = lodging_priceErr;
	}

	public String getLodging_imageErr() {
		return lodging_imageErr;
	}

	public void setLodging_imageErr(String lodging_imageErr) {
		this.lodging_imageErr = lodging_imageErr;
	}

	public boolean isResult() {
		return result;
	}

	public void setResult(boolean result) {
		this.result = result;
	}
	
}
