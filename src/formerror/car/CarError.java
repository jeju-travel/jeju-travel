package formerror.car;

public class CarError {
	private String car_nameErr;
	private String car_typeErr;
	private String car_priceErr;
	private String capacityErr;
	private String car_fuelErr;
	private String car_locErr;
	private String car_imgErr;
	private boolean result;
	
	public CarError() {}

	public String getCar_nameErr() {
		return car_nameErr;
	}

	public void setCar_nameErr(String car_nameErr) {
		this.car_nameErr = car_nameErr;
	}

	public String getCar_typeErr() {
		return car_typeErr;
	}

	public void setCar_typeErr(String car_typeErr) {
		this.car_typeErr = car_typeErr;
	}

	public String getCar_priceErr() {
		return car_priceErr;
	}

	public void setCar_priceErr(String car_priceErr) {
		this.car_priceErr = car_priceErr;
	}

	public String getCapacityErr() {
		return capacityErr;
	}

	public void setCapacityErr(String capacityErr) {
		this.capacityErr = capacityErr;
	}

	public String getCar_fuelErr() {
		return car_fuelErr;
	}

	public void setCar_fuelErr(String car_fuelErr) {
		this.car_fuelErr = car_fuelErr;
	}

	public String getCar_locErr() {
		return car_locErr;
	}

	public void setCar_locErr(String car_locErr) {
		this.car_locErr = car_locErr;
	}

	public String getCar_imgErr() {
		return car_imgErr;
	}

	public void setCar_imgErr(String car_imgErr) {
		this.car_imgErr = car_imgErr;
	}

	public boolean isResult() {
		return result;
	}

	public void setResult(boolean result) {
		this.result = result;
	}
	
	
	
}
